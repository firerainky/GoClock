func connectedToNetwork() -> Bool {

    var zeroAddress = sockaddr_in()
    zeroAddress.sin_len = UInt8(MemoryLayout<sockaddr_in>.size)
    zeroAddress.sin_family = sa_family_t(AF_INET)

    guard let defaultRouteReachability = withUnsafePointer(to: &zeroAddress, {
        $0.withMemoryRebound(to: sockaddr.self, capacity: 1) {
            SCNetworkReachabilityCreateWithAddress(nil, $0)
        }
    }) else {
        return false
    }

    var flags: SCNetworkReachabilityFlags = []
    if !SCNetworkReachabilityGetFlags(defaultRouteReachability, &flags) {
        return false
    }

    let isReachable = flags.contains(.reachable)
    let needsConnection = flags.contains(.connectionRequired)

    return (isReachable && !needsConnection)
}

func timeStamp(from time: Int) -> String {
    let hours = time / 3600
    let minutes = (time - hours * 3600) / 60
    let seconds = time - hours * 3600 - minutes * 60
    var result = ""
    if hours > 0 {
        result += String.localizedStringWithFormat(NSLocalizedString("%lld hours", comment: ""), hours)
    }
    if minutes > 0 {
        result += String.localizedStringWithFormat(NSLocalizedString("%lld minutes", comment: ""), minutes)
    }
    if seconds > 0 {
        result += String.localizedStringWithFormat(NSLocalizedString("%lld seconds", comment: ""), seconds)
    }
    
    return result
}

func timeStamp(from time: TimeInterval) -> String {
    let intTime = Int(time)
    var result = ""
    let hour = intTime / 3600
    let minute = (intTime - 3600 * hour) / 60
    let second = intTime - 3600 * hour - minute * 60
    if hour > 0 {
        result += String(format: "%02d:", hour)
    }
    if minute > 0 || hour > 0 {
        result += String(format: "%02d:", minute)
    }
    result += String(format: "%02d", second)
    return result
}

extension Color {
    init(hex: Int, alpha: Double = 1) {
        let components = (
            R: Double((hex >> 16) & 0xff) / 255,
            G: Double((hex >> 08) & 0xff) / 255,
            B: Double((hex >> 00) & 0xff) / 255
        )
        self.init(
            .sRGB,
            red: components.R,
            green: components.G,
            blue: components.B,
            opacity: alpha
        )
    }
}

